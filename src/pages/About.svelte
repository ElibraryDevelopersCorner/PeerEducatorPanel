        <p class="c1"><span class="c6">An Introspective Background</span></p>
        <p class="c1"><span class="c8">Peer education </span><span class="c9">is a popular concept that implies an
                approach,
                a communication channel, a methodology, a philosophy, and a strategy. The English term &#39;peer&#39;
                refers
                to &ldquo;one that is of equal standing with another; one belonging to the same societal group,
                especially
                based on age, grade or status&rdquo;. The term &#39;education&#39; refers to the
                &ldquo;development&rdquo;,
                &ldquo;training&rdquo;, or &ldquo;persuasion&rdquo; of a given person or thing, or the
                &ldquo;knowledge&rdquo; resulting from the educational process. This concept is introduced by CBSE in
                academic year 2021-22</span></p>
        <p class="c1"><span class="c9">Why Peer Education?</span></p>
        <p class="c1"><span class="c9">Trained peer educators are a more credible source of information for some youth
                than
                are adult educators because they communicate in readily understandable ways and serve as positive role
                models while dispelling misperceptions that most youths are problematic.</span></p>
        <p class="c1"><span class="c8">PIS Sangli</span><span class="c9">, always supports new ways to education. Peer
                education panel was formed under the guidance of Principal Sir, Mr. G.B. Patil. (Under SDP priority 2 ,
                Behavior and personality development, school has started this initiative).</span></p>
        <p class="c1"><span class="c4">Who are Peer Educators?</span></p>
        <p class="c1"><span class="c13">The term </span><span class="c8">&ldquo;peer educator&rdquo; </span><span
                class="c9">refers to true peers or near-peers. A person who is a leader or respected and can readily
                volunteer the program within the peer group is more likely to be influential.</span></p>
