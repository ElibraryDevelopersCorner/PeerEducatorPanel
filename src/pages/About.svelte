        <p class="c1"><span class="c4">School-Based Peer Education Program &ndash; Planning and Setting up</span></p>
        <p class="c1"><span class="c9">The peer education program is implemented at two levels in the school. The
                first</span></p>
        <p class="c1"><span class="c9">level is the selection and training of peer leaders. The second level is the
                conduction of schedule by the peer leaders in the school. The peer leaders were selected on the
                following
                attributes: expressing oneself, interest in group activity, leadership ability, self-confidence,
                creativity
                and knowledge of community issues.</span></p>
        <p class="c1"><span class="c8">Peer educator panel was formed</span><span class="c9">. After forming the panel,
                higher grade students came up with a lesson plan and they themselves had designed student&rsquo;s skill
                development plan. Later they improvised the idea of tutoring 527 new students. During bridge course,
                daily 1
                hour, they contributed for the same. Objective behind it was to make new students comfortable and
                develop
                skill. Tutoring plan was totally activity based and followed the lesson plan which was designed by the
                students under the guidance of the teachers.</span></p>
